package RNG4.am;


public class switch多分支练习1 {
 public static void main(String[] args) {
	        int rank=1;
	        switch(rank) {
	        case 1:
	        	System.out.println("获得笔记本电脑");
	        	break;
	        case 2:
	        	System.out.println("获得移动硬盘");
	        	break;
	        case 3:
	        	System.out.println("获得鼠标");
	        	break;
	        	default:             //默认
	        		System.out.println("没有奖励");
	        }
 }
}
