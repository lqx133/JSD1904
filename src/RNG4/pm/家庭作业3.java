package RNG4.pm;

import java.util.Scanner;

public class 家庭作业3 {
	public static void main(String[] args) {
	Scanner scan=new Scanner(System.in);
	System.out.println("请输入五个数字");
	double a=scan.nextDouble();
	double b=scan.nextDouble();
	double c=scan.nextDouble();
	double d=scan.nextDouble();
	double f=scan.nextDouble();
		if(a>=0) {
			System.out.println(a+"是正数");
		}else {
			System.out.println(a+"是负数");
		}
		if(b>=0) {
			System.out.println(b+"是正数");
		}else {
			System.out.println(b+"是负数");
		}
		if(c>=0) {
			System.out.println(c+"是正数");
		}else {
			System.out.println(c+"是负数");
		}
		if(d>=0) {
			System.out.println(d+"是正数");
		}else {
			System.out.println(d+"是负数");
		}
		if(f>=0) {
			System.out.println(f+"是正数");
		}else {
			System.out.println(f+"是负数");
		}
}
}